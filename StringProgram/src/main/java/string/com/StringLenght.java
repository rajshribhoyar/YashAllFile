package string.com;

public class StringLenght {
	public static void main(String[] args) {
		
		String StudentName="Rajshri Bhoyar";
		
		System.out.println(StudentName);
		
		//Syntax:- string.lenght()
		System.out.println(StudentName.length());
		
		System.out.println("Bhoyar".length());
	}

}
