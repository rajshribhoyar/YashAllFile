package string.com;

public class SubString {
	public static void main(String[] args) {
		String name="Rajshri";
		
		 // extract substring from index 0 to 2
		System.out.println(name.substring(0, 3)); // Raj
		System.out.println(name.substring(3,7)); // shri
		System.out.println(name.substring(0, 4));//Rajs
	}

}
